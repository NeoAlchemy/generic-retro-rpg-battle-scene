function shakeDino (dinoX: Sprite) {
    dinoX.setVelocity(-50, 0)
    pause(100)
    dinoX.setVelocity(0, 0)
    pause(50)
    dinoX.setVelocity(50, 0)
    pause(103)
    dinoX.setVelocity(0, 0)
}
